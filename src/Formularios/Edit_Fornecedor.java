    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nomeFornecedor = (String) jTextFieldnome.getText();
        //String cnpjFornecedor = (String) jFormattedTextFieldcnpj.getText();

        String ruaFornecedor = (String) jTextFieldrua.getText();
        String numerFornecedor = (String) jTextFieldnum.getText();
        String cidadeFornecedor = (String) jTextFieldcidade.getText();
        String complementoFornecedor = (String) jTextFieldref.getText();
        String cepFornecedor = (String) jFormattedTextFieldcep.getText();
        String bairroFornecedor = (String) jTextFieldbairro.getText();
        String telefoneFornecedor = (String) jFormattedTextFieldfixo.getText();
        String celulaFornecedor = (String) jFormattedTextFieldcelu.getText();
        String emailFornecedor = (String) jTextFieldemail.getText();
        String estadoFornecedor = (String) jComboBoxesta.getSelectedItem();
        
        
        String cnpjFornecedor = (String) jFormattedTextFieldcnpj.getText();

        // FAZENDO AS SEGURANÇAS DO SISTEMA:
        if (cnpjFornecedor.equals("  .   .   /    -  ")) {
            JOptionPane.showMessageDialog(null, "O CNPJ não pode ser vazio");
            return;
        } 
   

        try {
        Fornecedor novoForn = new Fornecedor(cnpjFornecedor, nomeFornecedor, telefoneFornecedor, celulaFornecedor, ruaFornecedor, numerFornecedor, estadoFornecedor, complementoFornecedor, cepFornecedor, emailFornecedor, bairroFornecedor, cidadeFornecedor, null);

        Session session = conexao.NewHibernateUtil.getSessionFactory().getCurrentSession();
        session.beginTransaction();

        session.update(novoForn);
        session.getTransaction().commit();
        
        JOptionPane.showMessageDialog(null, "Forncedor Atualizado");
        
        } catch(Exception e) {
           // JOptionPane.showMessageDialog(null, "Cliente ja cadastrado");
        }
        //===================================================================

    }//GEN-LAST:event_jButton1ActionPerformed

    
    
    
    
