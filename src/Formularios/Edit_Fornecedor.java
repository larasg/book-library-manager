    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         String cnpjFornecedor = (String) jFormattedTextFieldcnpj.getText();
        
       // FAZENDO AS SEGURANÇAS DO SISTEMA: 
        if (cnpjFornecedor.equals("  .   .   /    -  ")) {
            JOptionPane.showMessageDialog(null, "INFORME O CNPJ");
            return;
        } 
        
        Session session = conexao.NewHibernateUtil.getSessionFactory().getCurrentSession();
        session.beginTransaction();
        
        Query select = session.createQuery("from Fornecedor where cnpjFornecedor = '" + cnpjFornecedor + "'");
        List fornecedor = select.list();
        
        if (fornecedor.isEmpty())         
            return;        
       
        
        //=====================================================================
        
        Fornecedor fr = (Fornecedor)fornecedor.get(0);
        System.out.println(fr.getCnpjFornecedor());
        
        jTextFieldnome.setText(fr.getNomeFornecedor());
        jFormattedTextFieldfixo.setText(fr.getTelefoneFornecedor());
        jFormattedTextFieldcelu.setText(fr.getCelulaFornecedor());
        jTextFieldemail.setText(fr.getEmailFornecedor());

        jTextFieldrua.setText(fr.getRuaFornecedor());
        jTextFieldnum.setText(fr.getNumerFornecedor());
        jTextFieldbairro.setText(fr.getBairroFornecedor());
        jFormattedTextFieldcep.setText(fr.getCepFornecedor());
        jTextFieldref.setText(fr.getComplementoFornecedor());
        
        // HABILITANDO OS CAMPOS DO FORM
       //    na propriedades da jframe retirei a opcao enable e agora estou habilitando ela 
        jTextFieldnome.setEnabled(true);
        jFormattedTextFieldfixo.setEnabled(true);
        jFormattedTextFieldcelu.setEnabled(true);
        jTextFieldemail.setEnabled(true);

        jTextFieldrua.setEnabled(true);
        jTextFieldnum.setEnabled(true);
        jTextFieldbairro.setEnabled(true);
        jFormattedTextFieldcep.setEnabled(true);
        jTextFieldref.setEnabled(true);
      
        
      //===================================================================  
        
        System.out.println(fornecedor);
        session.getTransaction().commit();
        session.close();

    }//GEN-LAST:event_jButton3ActionPerformed

    
    
    
    
    
    
    
